Number.prototype.add = function(num) {
    return this + num
}

Number.prototype.sub = function(num) {
    return this - num
}

Number.prototype.mul = function(num) {
    return this * num
}

Number.prototype.div = function(num) {
    return this / num
}

Number.prototype.pow = function(num) {
    return this ** num
}

Number.prototype.sqr = function() {
    return this ** 0.5
}